num1 = int(input("Enter the First Number:"))
num2 = int(input("Enter the Second Number:"))

print("Num1 and Num2 is Equal :",num1 == num2)
print("Num1 and Num2 is Not Equal :",num1 != num2)
print("Num1 is greater than Equal to Num2 :",num1 >= num2)
print("Num1 is Less than Equal to Num2 :",num1 <= num2)
print("Num1 is greater than Num2 :",num1 > num2)
print("Num1 is Less than Num2 :",num1 < num2)